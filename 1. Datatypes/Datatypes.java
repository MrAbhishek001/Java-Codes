public class Datatypes {
    public static void main(String[] args) {
        byte b = 8; // Size = 1 byte
        int number = 2; // Size = 4 byte
        char ch = 'a'; // Size = 2 byte
        boolean var = true; // Size = 1 byte
        double price = 10.5; // Size = 8 byte
        float rating = 10.5f;  // For floating values just add f at last of number.
        // long : It is used to store large numbers // Size = 8 byte
        // float : For decimal Numbers // Size = 4 byte
        // short : For small integer values // Size = 2 byte
        System.out.println(b + " " + number + " " + ch + " " + var + " " + price + " " + rating);
    }
}